#region Using Directives
using System;
using System.Threading;
using Microsoft.ServiceFabric.Actors.Runtime; 
#endregion

namespace Microsoft.AzureCat.Samples.ResourceMutexActorService
{
    internal static class Program
    {
        /// <summary>
        /// This is the entry point of the service host process.
        /// </summary>
        private static void Main()
        {
            try
            {
                // This line registers an Actor Service to host your actor class with the Service Fabric runtime.
                // The contents of your ServiceManifest.xml and ApplicationManifest.xml files
                // are automatically populated when you build this project.
                // For more information, see http://aka.ms/servicefabricactorsplatform

                ActorRuntime.RegisterActorAsync<ResourceMutexActor>(
                    (context, actorType) => new ResourceMutexActorService(
                        context,
                        actorType,
                        () => new ResourceMutexActor(),
                        null,
                        new ActorServiceSettings
                        {
                            ActorGarbageCollectionSettings = new ActorGarbageCollectionSettings(300, 60)
                        })).GetAwaiter().GetResult();

                Thread.Sleep(Timeout.Infinite);
            }
            catch (Exception e)
            {
                ActorEventSource.Current.ActorHostInitializationFailed(e.ToString());
                throw;
            }
        }
    }
}
